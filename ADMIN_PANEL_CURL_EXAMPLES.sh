echo "    \"monthlyUsers\": 5000,"
echo "    \"iconEmoji\": \"🚀\","
echo "    \"telegramUrl\": \"https://t.me/mybot_updated\""
echo "  }' | jq ."
echo ""

# 6. Delete Bot (requires token)
echo "6️⃣  Delete Bot (Requires Authentication)"
echo "$ TOKEN='admin-token-$ADMIN_PASSWORD'"
echo "$ BOT_ID=1"
echo "$ curl -X DELETE $BASE_URL/admin/bots/\$BOT_ID \\"
