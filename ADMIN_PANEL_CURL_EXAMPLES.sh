echo "    \"categoryId\": 1,"
echo "    \"rating\": 4.5,"
echo "    \"reviewCount\": 10,"
echo "    \"isVerified\": true,"
echo "    \"isPremium\": false,"
echo "    \"tags\": [\"utility\", \"fun\"],"
echo "    \"monthlyUsers\": 1000,"
echo "    \"iconEmoji\": \"🤖\","
echo "    \"telegramUrl\": \"https://t.me/mybot\""
echo "  }' | jq ."
echo ""

# 5. Update Bot (requires token)
echo "5️⃣  Update Bot (Requires Authentication)"
echo "$ TOKEN='admin-token-$ADMIN_PASSWORD'"
echo "$ BOT_ID=1"
echo "$ curl -X PUT $BASE_URL/admin/bots/\$BOT_ID \\"
echo "  -H 'Content-Type: application/json' \\"
echo "  -H \"Authorization: Bearer \$TOKEN\" \\"
echo "  -d '{"
echo "    \"username\": \"@mybot_updated\","
echo "    \"name\": \"My Updated Bot\","
echo "    \"description\": \"Updated description\","
echo "    \"categoryId\": 1,"
